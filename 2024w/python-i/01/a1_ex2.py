a = int(input('a: '))
b = int(input('b: '))
c = int(input('c: '))
d = int(input('d: '))

print(f'Sum of a, b and d: {a + b + d}')

print(f'Product of all numbers: {a * b * c * d}')

print(f'The sum of a and c times the sum of b and d: {(a + c) * (b + d)}')

print(f'a divided by c (int): {a // c}')

print(f'a divided by b (float): {a / b}')

print(f'Remainder of a divided by d: {a % d}')

print(f'a to the power of -c: {a ** -c}')

print(f'd to the power of 1/2 (square root): {d ** 0.5}')

print(f'Complex equation: {(c / 3) * (a ** (b + (c / 2)) - 1) + d}')
